"""Funkcje matematyczne"""


def add(a: int, b: int) -> int:
    """suma 2 liczb"""
    return a + b


def subtract(a: int, b: int) -> int:
    """roznica 2 liczb"""
    return a - b


def multiply(a: int, b: int) -> int:
    """iloczyn 2 liczb"""
    return a * b


def divide(a: int, b: int) -> float:
    """iloraz 2 liczb"""
    return a / b
